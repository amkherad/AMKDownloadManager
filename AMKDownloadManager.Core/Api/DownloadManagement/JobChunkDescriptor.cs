using System;

namespace AMKDownloadManager.Core.Api.DownloadManagement
{
    public class JobChunkDescriptor
    {
        public IJob Job { get; }
        public IJobChunk JobChunk { get; }

        public JobChunkDescriptor(IJob job, IJobChunk jobChunk)
        {
            Job = job;
            JobChunk = jobChunk;
        }
    }
}